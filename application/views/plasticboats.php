                        <h2>HOW TO BUILD A KAYAK USING DISCARDED PLASTIC BOTTLES</h2><br>
                        <strong>10/03/2017</strong><br>
                        <img class="article_pic1" src="../../assets/img/plastic-bottles-recycling-ideas-43.jpg" alt="Plastic Bottles"><br><br>
                        <h3>Difficulty Level:</h3><br>
                        <p>Moderately Challenging: May require expertise in few areas</p>
                        <h3>Time required</h3><br>
                        <p>It depends on your craftsmanship and expertise. If you havenâ€™t built any contraptions as yet it could take some time. Set aside 2-7 days for the construction.</p>
<h3>Resources required</h3><br>

<ul>
                           <li>Metal wire mesh.</li>
                           <li> Plastic bottles.</li>
                           <li>Tarp. </li>
                           <li>Plywood</li>
    <li>Pole</li>
    <li>Wooden,plastic and metal junk or scrap pieces</li>
    <li>Duct tape</li>
    <li>Rope</li>
    <li>Glue</li>
    <li>Scissors</li>
                        </ul>
                        
                        <h3>Instructions</h3>
                        <ul>
                        <li>You will be using a metal wire mesh as the base and plastic bottles will be fastened below it to keep your dinghy afloat.</li>
                           <li> First, lay out the wire mesh on the ground. Decide upon the dimensions of your kayak and cut out the base accordingly. Add a few bottles to the base and see how much distance they cover. Estimate the total number of bottles required to build 3-4 layers.  </li>
                           <li>Check out the water bottles. Make sure that they are empty and clean. Segregate the ones that have caps from the bottles without caps. A sizable number is needed to bear the body weight of passengers. Now fasten the bottles with caps to the wire mesh with ropes or duct tapes. </li>
                           <li>It is really important to add more weigh at the bottom, centered towards the middle of the boat. Materials like plastic, metal, and wood should be added to the bottom of the ferry. Since this is a recycling project, try to use junk or disposed things made from these materials.</li>
                           <li> Use a tarp or any other plastic sheet to seal the bottom half after you have finished construction. Measure and cut out then pull it over and secure tight with a zipper, glue or duct tape.</li>
                           <li> A paddle for your sailboat is a must. You can use an old one discarded or build a new one from plywood.</li>
                                  
