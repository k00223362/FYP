                        <h2>Prince Charles calls for plastic bottle deposit scheme
</h2><br>
                     <strong>   <p>Prince Charles has given his support for a plastic bottle deposit scheme to reduce waste entering the sea.

</p></strong>
                        <strong>10/03/2017</strong><br>

                        <img class="article_pic1"   src="<?php echo $img_base . "/assets/img/Prince-Charles2.jpg"?>" alt="Plastic Bottles"><br><br>
                        
                        <em>Royal Visit Washington</em>
                        <br>
                       
                           <p> Britain is thought to recycle less than 60% of its plastic bottles, compared to 98% in Germany.
                           </p><br>
                           <p> The government is said to be considering plans for a plastic bottle levy to reduce waste at landfill sites and in the sea. </p>
                        <p> Reports say plans include charging consumers an extra 10-20p per plastic bottle or container purchased, which can be replaced as part of a deposit return scheme.</p>
                          <p>Such schemes are firmly established in Europe, and North America, and has been trialled in Scotland too.</p>
                        <p>While a tax similar to the plastic bag charge is thought to have been ruled out, ministers are said to be pouring over the results of the deposit return scheme trial in Scotland.</p>
                        
                        <p>Countries such as Germany and Sweden often have ‘reverse vending machines’ in supermarkets that give credits when discarded plastic bottles are deposited.</p>
                        
                        <p>Prince Charles recently told Sky News: “One of the most troubling environmental trends, as I see it at least, is the progressive build-up of plastic waste in the oceans.</p>
                        
                        <p>Eighty percent of it is coming from the land as everyday products such as plastic bags, straws and bottles as they travel along the rivers to the sea and finish up being spread right around the world, even on the most remote stretches of oceans, the farthest-flung beaches on earth and the deepest reaches of the ocean.</p>
                        
                        <p>“Some of that plastic is being swallowed by sea birds, by whales, by dolphins, seals and other creatures to the point today where plastic waste is estimated to be in the guts of all marine mammals.”</p>
                        
                        
                        <p>He added his support to the scheme. “It always seems extraordinary to me we don’t take these sorts of initiatives which can make a dramatic difference.”</p>
                        
                        <p>Part of environment minister Therese Coffey’s new litter strategy may well address the issue of one-use plastic bottles.</p>
                        
                        
                        <p>Conservative MP Steve Double recently asked how ministers plan to cut plastic on British beaches, and Coffey responded: “The consultation is out there on micro-beads but there is in that a call for wider evidence about the need to tackle other plastics. We are developing a new litter strategy which may well address this issue.”</p>
                        
                        <p>Jane Bickerstaffe, director of INCPEN, said the organisation believed the solution is to prevent all litter by making littering completely socially unacceptable.</p>
                        
                        
                        <p>“Deposit schemes do not influence litterers. For example, despite having a deposits scheme for over 20 years, the Swedish government had to increase the cost of on-the-spot fines for littering drinks containers in 2011 in an attempt to prevent them being littered. South Australia has also had a scheme for many years but there are still 45 littered items per 1,000m2 compared with only 25 in Victoria which has a comprehensive behaviour change programme that tackles all litter.”</p>
                        
                        
                        <p>She added that deposit systems are expensive (for consumers, industry and councils), address only a proportion of litter, divert drink containers away from kerbside collection to a parallel system that requires more lorries and increases environmental impact.</p>
                        
                        <p>“In 2013, Scotland spent £1m trialling deposit schemes. In the best trial, only 38% of containers were returned. This shows that there is no point in bringing back a scheme that has had its day.”</p>
                        
                        <p>Martin Kersh, FPA executive director, said bottles were an easy target and wanted all parties to examine how those bottles arrive in oceans and on beaches.</p>
                           
                        <p>“It is human behaviour that leads to their presence. The imposition of a deposit system on bottles will not change the behaviour of those who feel it is accepted to litter. Society must do this and to focus on the drinks brands and their packaging suppliers is a distraction form the key task of making littering totally unacceptable. A deposit scheme will be place a huge financial burden on business and has health risks should deposits have to be returned at the till in environments where ready to eat food is served.”</p>
                         
                        <p>While some MPs have cautioned against charging consumers, Samantha Harding, litter programme director at the Campaign to Protect Rural England, welcomed the idea.</p>
                        
                        <p>“Billions of bottles and cans are thrown away every year, harming our wildlife and polluting the environment. After the positive effects of the plastic bag charge, the obvious next step is a bottle deposit return system. The bag charge’s success has shown emphatically that people will get behind these schemes. The Minister’s comments are an encouraging sign that the Government is committed to reducing bottle litter and pollution.”</p>

                           
