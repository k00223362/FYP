                        <h2>How to Build a House Using Plastic Bottles</h2><br>
                        <strong>10/03/2017</strong><br>
                        <img class="article_pic1" src="../../assets/img/Plastic-Bottle-House-05.jpg" alt="Plastic Bottles"><br><br>
                        <h3>Materials</h3><br>
                       <ul>
                           <li> Lots of Plastic Bottles.</li>
                           <li> Dry Sand.</li>
                           <li> Sawdust.</li>
                           <li>Clay.</li>
                           <li> Iron / Steel Rods.</li>
                           <li> Cement.</li>
                           <li>Rolls of Thick Thread.</li>
                           
                           
                           <br>
                        </ul>
                        
                        <h3>Purpose</h3>
                        <iframe width="560" height="315" src="https://www.youtube.com/embed/hTto3_jV894" frameborder="0" allowfullscreen></iframe> <br><br><br>
                        <p>
                        The picture above is of an ecological house constructed in Honduras using some 8,000 PET bottles, in the process freeing up an estimated 12 cubic meters (m3) in the local landfill.

The house also features a living roof (sometimes called a green roof) of sod and turf. Such roofs not only have aesthetic appeal, but tend to insulate the house better than conventional roofs, lowering heating and cooling costs. They are also cheaper than conventional roofs.
                        
                       
                         </p>
                        <h3>Steps</h3>
                        <ul><li>Your first step is to collect as many discarded plastic bottles as you can. They can be of various capacities.</li>
                        <li>Next step is to find a place where you can get a hell load of dry sand (the finer, the better).</li>
                        <li>Take the bottles, fill them with sand, seal and then paste them with a mixture made of earth, clay, sawdust and a little cement to provide additional strength and durability. </li>
                        <li>Now you are all set. Always start with the pillar. You can have as many pillars as you want, but not less than three.</li> 
                       <li>Dig a cylindrical pit about 60 cm in depth.The radius should be at least 10 cm more than the length of the bottle that you are going to use for constructing the pillar.</li>
                        <li>Make a cement bed (about 4 cm thickness) in the pit. Insert an long iron/steel rod at the centre. Tie a long and strong thread to the rod. Now we are going to lay 10 or 11 bottles around this rod. For this, first lay a bottle on the bed such that the bottle is radially oriented towards the centre. (i.e., the cap is oriented towards the rod).</li>
                        <li>Take the thread and make a knot around the neck of the bottle you just laid. Put the next bottle in similar way keeping in mind that the caps of the 2 bottles touch each other. Make a knot around the 2nd bottle also. Repeat the steps until you lay all the 11 bottles. You have made your first course of bottles.</li>
                        <li>Fill in the gaps between the bottles with rubble and cement. Make the next bed and continue this process till you fill the entire pit. Hurray! Your foundation is complete. Leave it for a few hours so that the cement will solidify.<li>
                        <li>After you have made the foundation for all your pillars,you can start building the pillar. Lay the bottles like you did for the foundation,and you will see your pillars rising. You can put broken bricks or other construction wastes between the bottles so that they don’t move.</li>
                        <li>Now if you want you can plaster your pillar with cement.This pillar is many times stronger much cheaper than an ordinary pillar made with bricks.</li>
                            <li>Make an outline of your platform with chalk powder or any color powders. Make a clay bed about 5 cm height and width = 5 cm > length of the bottles. This bed runs along the perimetre of the platform.</li>
                         <li>Now lay the bottles (with their neck facing outwards) one by one on this bed and tie them to each other as shown in the picture.</li>
                            <li>After laying a course, fill the inside area with cement, clay or rubble. If you want to make more courses, make sure that you knot each bottle of the underlying course with the above course as shown in the picture. This is to impart more strength to the structure.</li>
                         <li>This is your platform. Now put tiles or ceramics for flooring.</li><br>
                            </ul>
<h4>Conclusion</h4>
<p>You can put any type of roof over this because the pillars are really strong enough to hold them.<br>

Create your own designs, and build your own structures with this cheap construction method!<br>

